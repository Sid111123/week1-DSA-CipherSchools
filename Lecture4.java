import java.util.*;

class Lecture4 {
    public static void main(String ar[]) {
        int x = 10;
        int y = 15;
        System.out.println("X = " + x);
        System.out.println("Y = " + y);
        byte b = 12;
        System.out.println("b = " + b);
        // Typecasting
        byte c = (byte) 200;
        System.out.println("c = " + c);

        int i = c;
        System.out.println("i = " + i);

        float f = (float) 8.75;
        System.out.println("f = " + f);
    }
}
